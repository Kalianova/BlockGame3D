int		ft_atoi(const char *nptr)
{
	int			i;
	int			k;
	long long	res;

	i = 0;
	k = 1;
	res = 0;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		i++;
		if (nptr[i - 1] == '-')
			k = -1;
	}
	while (res <= 2147483648 && nptr[i] && nptr[i] >= '0' && nptr[i] <= '9')
	{
		res = res * 10 + (nptr[i] - '0');
		i++;
	}
	if (k == -1)
		return (res > 2147483648 ? 0 : -res);
	return (res > 2147483647 ? -1 : res);
}
